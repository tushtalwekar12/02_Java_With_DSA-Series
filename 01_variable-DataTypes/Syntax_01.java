// initialization and declaration of variable -
public class Syntax_01{   // public class 
    public static void main(String[] args){  // main function
        int a = 10;
        int b = 20;
        int sum = a + b;
        System.out.println(sum);

        String name = "Tushar"; 
        System.out.println(name);

        int c; //declaration variable of int dataType
        c = 30;  // initialize the value to variable c 
        System.out.println(c); // print c

        int d = 1;
        d = 2; // update value
        System.out.println("updated value :" + d); // 2

    }
}